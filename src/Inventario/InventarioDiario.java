    private void ListarProductosSolicitados(int tipo) throws Exception {
        ArrayList<ConsultaProductos> result = ListarUpdate.ListarProductos(Fecha,SucursalUtil.obtenerSucursal(),tipo);
        RecargarTabla(result);
    }

    private void RecargarTabla(ArrayList<ConsultaProductos> list) {
        Object[][] datos = new Object[list.size()][4];
        int i = 0;
        for (ConsultaProductos t : list) {
            datos[i][0] = t.getCODIGO();
            datos[i][1] = t.getDESCRIPCION();
            datos[i][2] = t.getCANTIDAD();
            datos[i][3] = t.getMEDIDA();

            i++;
        }

        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(
                datos,
                new String[]{"CODIGO", "DESCRIPCION", "CANTIDAD", "MEDIDA"}
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return column == 2; // Solo se puede editar CANTIDAD
            }
        };

        ListaPro.setModel(model);
        ListaPro.getColumnModel().getColumn(1).setCellRenderer(new TextAreaRenderer());
        ListaPro.getColumnModel().getColumn(0).setPreferredWidth(-20);
        ListaPro.getColumnModel().getColumn(1).setPreferredWidth(255);
        ListaPro.getColumnModel().getColumn(2).setPreferredWidth(55);

        // Aquí se agrega el listener para detectar edición
        model.addTableModelListener(new TableModelListener() {
            @Override
            public void tableChanged(TableModelEvent e) {
                if (e.getType() == TableModelEvent.UPDATE && e.getColumn() == 2) {
                    int row = e.getFirstRow();
                    int modelRow = ListaPro.convertRowIndexToModel(row);
                    int codigo = Integer.parseInt(
                            String.valueOf(model.getValueAt(modelRow, 0))
                    );
                    double cantidadRaw = Double.parseDouble(
                            String.valueOf(model.getValueAt(modelRow, 2))
                    );
                    double cantidad = Math.round(cantidadRaw * 100.0) / 100.0;

                   // int id_pedido = Integer.parseInt(NoPedido.getText());
                    System.out.println(cantidad+"--"+codigo+"--"+Fecha);
                  ListarUpdate.actualizarCantidad(codigo,cantidad,Fecha,SucursalUtil.obtenerSucursal());//cantidad,codigo,fecha
                }
            }
        });
    }
    
    

